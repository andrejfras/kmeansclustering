
public class Point {
    private double la;
    private double lo;

    public Point(double la, double lo) {
        this.la = la;
        this.lo = lo;
    }

    public double getLa() {
        return la;
    }

    public double getLo() {
        return lo;
    }

    public void setLa(double la) {
        this.la = la;
    }

    public void setLo(double lo) {
        this.lo = lo;
    }
}

